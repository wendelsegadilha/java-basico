package br.com.wendelsegadilha.aula13;

public class OperadoresAritimeticos {

	public static void main(String[] args) {
		
		int valor01 = 10;
		int valor02 = 4;
		
		int resultado = valor01 + valor02;
		System.out.println(resultado);
		
		resultado = valor01 - valor02;
		System.out.println(resultado);
		
		resultado = valor01 * valor02;
		System.out.println(resultado);
		
		resultado = valor01 / valor02;
		System.out.println(resultado);
		
		resultado = valor01 % valor02;
		System.out.println(resultado);

	}

}
