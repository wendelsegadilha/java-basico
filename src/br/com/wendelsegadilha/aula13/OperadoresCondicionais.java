package br.com.wendelsegadilha.aula13;

public class OperadoresCondicionais {

	public static void main(String[] args) {
		
		int valor01 = 10;
		int valor02 = 4;
		
		boolean resultado;
		
		resultado = valor01 == valor02;
		System.out.println(resultado);
		
		resultado = valor01 != valor02;
		System.out.println(resultado);
		
		resultado = valor01 > valor02;
		System.out.println(resultado);

		resultado = valor01 < valor02;
		System.out.println(resultado);
		
		resultado = valor01 >= valor02;
		System.out.println(resultado);
		
		resultado = valor01 <= valor02;
		System.out.println(resultado);
		
		//OPERADOR TERNÁRIO
		boolean teste = false;
		System.out.println(teste);

	}

}
